## 3. AITLロジックとの接続

- AITLはAI SoC内に搭載され、以下を統括：
  - センサ正規化処理（前処理）
  - 条件判断ルールの実行（例：灌水、通信切替）
  - 出力制御（PWM／GPIO／通信指令）

- 全ての入力データ（センサ、画像、通信状態）をAITLが仲介

---

## 4. 教育対応設計

- PoCDK評価環境で各モジュールの状態をリアルタイム可視化
- GUIによるAI判断過程の観察、ルールの変更・再学習対応
- 異常値シミュレーション、センサ模擬入力、灌水制御可視化など搭載

---

## 5. 応用シナリオ例

| 状況                    | AITL判断                          | 制御出力           |
|-------------------------|-----------------------------------|--------------------|
| 土壌水分が不足          | 「灌水ON」                        | 拡張I/O → バルブON |
| 日照強・湿度高・CO₂高   | 「ハウス換気ON」                 | GPIO → ファン駆動  |
| 果実成熟を画像で検出    | 「収穫ドローン起動」             | 拡張I/O → Drone Tx |
| バッテリ残量20%以下     | 「通信モード → LoRa節電切替」   | 通信モジュール切替 |
| センサ異常値を検出      | 「センサ校正モードへ移行」       | GUIに通知          |

